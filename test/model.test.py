URL = "http://38.46.219.171:5431/claude-kiro-oauth/v1/messages"
HEADERS = {
    "Content-Type": "application/json",
    "X-Api-Key": "4r1OZ9sxTgN4G2RJJiE5I2DtecXbnkGe"
}

def send_question(question, index):
    """发送单个问题的请求"""
    # 构造请求体
    data = {
        "model": "claude-sonnet-4-5",
        "max_tokens": 4096,
        "messages": [
            {
                "role": "user",
                "content": question
            }
        ]
    }
    
    try:
        # 发送POST请求
        response = requests.post(
            url=URL,
            headers=HEADERS,
            data=json.dumps(data, ensure_ascii=False)  # ensure_ascii=False 保证中文正常传输
        )
        # 打印请求信息
        print("\n======================================")
        print(f"正在发送第 {index+1} 个请求：{question}")
        print("======================================")
        # 打印响应状态和内容（可选，可根据需要注释）
        print(f"响应状态码：{response.status_code}")
        print(f"响应内容：{response.text[:500]}...")  # 只打印前500个字符，避免内容过长
    except Exception as e:
        # 异常处理，避免单个请求失败导致整个程序终止
        print(f"\n❌ 第 {index+1} 个请求发送失败：{str(e)}")

